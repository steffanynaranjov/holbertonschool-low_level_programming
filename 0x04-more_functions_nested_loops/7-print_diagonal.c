#include "holberton.h"
/**
* print_diagonal - Draws a diagonalt line in the terminal
* _putchar - Prints characters one by one
* @n: Parameter of times
* 
* Description: Draws a diagonal line
* Return: 0 Value
*/
void print_line(int n)
{
int y, z;

for (y = 0; y < y; y++)
{
for (z = 0; z < z; z++)
_putchar(' ');
_putchar('\\');
_putchar('\n');
}
if (n < 1)
_putchar('\n');
}
