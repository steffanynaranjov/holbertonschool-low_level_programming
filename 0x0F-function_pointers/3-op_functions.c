/**
* op_add - add two int
* @a: number
* @b: number
*
* Return: int
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
* op_sub - substract two int
* @a: number
* @b: number
*
* Return: int
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
* op_mul - multiply two int
* @a: number
* @b: number
*
* Return: int
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
* op_div - divide two int
* @a: number
* @b: number
*
* Return: int
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
* op_mod - mod two int
* @a: number
* @b: number
*
* Return: int
*/
int op_mod(int a, int b)
{
return (a % b);
}
